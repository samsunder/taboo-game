// Difficulty distribution for each mode
const DIFFICULTY_DISTRIBUTIONS = {
  easy: { easy: 0.7, normal: 0.2, hard: 0.1, insane: 0 },
  normal: { easy: 0, normal: 0.7, hard: 0.3, insane: 0 },
  hard: { easy: 0.1, normal: 0.2, hard: 0.7, insane: 0 },
  insane: { easy: 0, normal: 0.2, hard: 0.2, insane: 0.6 },
  mixed: { easy: 0.25, normal: 0.25, hard: 0.25, insane: 0.25 },
};

// Helper to shuffle an array
function shuffleArray(array) {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

// Get words for a specific difficulty with weighted distribution
function getWordsForDifficulty(difficulty, totalWords = 100) {
  const distribution = DIFFICULTY_DISTRIBUTIONS[difficulty];

  if (!distribution) {
    // Fallback to mixed if invalid difficulty
    return getWordsForDifficulty('mixed', totalWords);
  }

  const selectedWords = [];

  // Calculate how many words from each difficulty level
  const counts = {
    easy: Math.round(totalWords * distribution.easy),
    normal: Math.round(totalWords * distribution.normal),
    hard: Math.round(totalWords * distribution.hard),
    insane: Math.round(totalWords * distribution.insane),
  };

  // Adjust for rounding errors to ensure we get exactly totalWords
  const totalCount = counts.easy + counts.normal + counts.hard + counts.insane;
  if (totalCount !== totalWords) {
    // Find the largest non-zero category and adjust it
    const categories = ['easy', 'normal', 'hard', 'insane'];
    const sortedCategories = categories
      .filter(cat => counts[cat] > 0)
      .sort((a, b) => counts[b] - counts[a]);
    if (sortedCategories.length > 0) {
      counts[sortedCategories[0]] += (totalWords - totalCount);
    }
  }

  // Select random words from each difficulty level
  for (const [level, count] of Object.entries(counts)) {
    if (count > 0 && WORD_LISTS[level]) {
      const shuffled = shuffleArray(WORD_LISTS[level]);
      selectedWords.push(...shuffled.slice(0, count));
    }
  }

  // Shuffle the final selection so difficulties are mixed
  return shuffleArray(selectedWords);
}

